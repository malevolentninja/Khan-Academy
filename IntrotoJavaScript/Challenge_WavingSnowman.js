// Make a waving snowman using the ellipse(),  rect() and line() commands

rect(1, 370, 398, 30); // block snowman is standing on
ellipse(200, 300, 150, 150); // bottom circle
ellipse(200, 200, 100, 100); //middle circle
ellipse(200, 120, 75, 75); // top circle
line(160,200,10,100); // left arm
line(240,200,350,90); // right arm
